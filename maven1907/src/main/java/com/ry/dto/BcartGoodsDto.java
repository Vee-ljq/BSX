package com.ry.dto;

import com.ry.pojo.BCart;
import com.ry.pojo.BGoods;
import com.ry.pojo.BGoodsWithBLOBs;
import com.ry.pojo.SPackagetype;
import com.ry.pojo.STastetype;

public class BcartGoodsDto extends BCart {

	private BGoodsWithBLOBs bloBs;
	private STastetype taste;
	private SPackagetype spackage;
	private Double money;
	
	public Double getMoney() {
		return money;
	}

	public void setMoney(Double money) {
		this.money = money;
	}

	public STastetype getTaste() {
		return taste;
	}

	public void setTaste(STastetype taste) {
		this.taste = taste;
	}

	public SPackagetype getSpackage() {
		return spackage;
	}

	public void setSpackage(SPackagetype spackage) {
		this.spackage = spackage;
	}

	public BGoodsWithBLOBs getBloBs() {
		return bloBs;
	}

	public void setBloBs(BGoodsWithBLOBs bloBs) {
		this.bloBs = bloBs;
	}
	
	
}
